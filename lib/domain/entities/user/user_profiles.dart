
class UserProfiles {
  final String userId;
  final String username;
  final String name;
  final double rating;
  final String email;
  final String bio;
  final bool verified;
  final String role;
  final String profilePictureUrl;
  final String origin;

  UserProfiles({
    required this.userId,
    required this.username,
    required this.name,
    required this.rating,
    required this.email,
    required this.bio,
    required this.verified,
    required this.role,
    required this.profilePictureUrl,
    required this.origin
  });

}